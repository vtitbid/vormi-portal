private fun SimplePanel.iconSvg(classname: String, vararg pathDList: String, fillRule: String? = null) {
    tag(TAG.SVG, className = "bi $classname me-2") {
        width = 16.px
        height = 16.px

        setAttribute("xmlns", "http://www.w3.org/2000/svg")
        setAttribute("fill", "currentColor")

        pathDList.forEach { path ->
            customTag("path") {
                fillRule?.let {
                    setAttribute("fill-rule", it)
                }

                setAttribute("d", path)
            }
        }
    }
}